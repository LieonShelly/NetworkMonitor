import Darwin

// MARK: - XOR Runtime Decryption

/// XOR 解密：将编译期加密的字节数组在运行时还原为字符串。
/// 使用完毕后调用方应立即将持有该字符串的变量置为 nil，
/// 并通过 eraseString(_:) 清零底层缓冲区。
@inline(__always)
func xorDecrypt(_ bytes: [UInt8], key: UInt8) -> String {
    let decoded = bytes.map { $0 ^ key }
    return String(bytes: decoded, encoding: .utf8) ?? ""
}

/// 将字符串底层 UTF-8 缓冲区用零字节覆盖，降低内存扫描风险。
@inline(__always)
func eraseString(_ str: inout String) {
    let len = str.utf8.count
    str.withUTF8 { ptr in
        let mutable = UnsafeMutableRawPointer(mutating: ptr.baseAddress)
        memset(mutable, 0, len)
    }
    str = ""
}

